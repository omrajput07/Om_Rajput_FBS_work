# Numeric data type
#1. int
i = 10
print("i:", i, type(i))
#2. float
f = 10.5
print("f:", f, type(f))
#3. complex
c = 3 + 4j
print("c:", c, type(c))
# text data type
#1. str
s = "Hello, World!"
print("s:", s, type(s))
# sequence data type
#1. list
l = [1, 2, 3, 4, 5]
print("l:", l, type(l))
#2. tuple
t = (1, 2, 3, 4, 5)
print("t:", t, type(t))
#3. range
r = range(1, 6)
print("r:", list(r), type(r))  # Convert range to list for display
# mapping data type
#1. dict
dict = { 1: "python" ,2: "java", 3: "c++" }
print("dict:", dict, type(dict))
# set data type
#1. set
s = {1, 2, 3, 4, 5}
print("s:", s, type(s))
# frozenset
#1. frozenset
fs = frozenset([1, 2, 3, 4, 5])
print("fs:", fs, type(fs))
# boolean data type
#1. bool
c = True
print("c:", c, type(c))
#2. bool
b = False
print("b:", b, type(b))
# None data type
#1. NoneType
n = None
print("n:", n, type(n))
